package com.gazi.gazi_renew.mock;

import com.gazi.gazi_renew.issue.domain.Issue;
import com.gazi.gazi_renew.issue.domain.Like;
import com.gazi.gazi_renew.issue.service.port.LikeRepository;
import com.gazi.gazi_renew.member.domain.Member;

import java.util.ArrayList;
import java.util.List;
import java.util.Objects;
import java.util.Optional;
import java.util.concurrent.atomic.AtomicLong;

public class FakeLikeRepository implements LikeRepository {
    private final AtomicLong autoGeneratedId = new AtomicLong(0);
    private final List<Like> data = new ArrayList<>();


    @Override
    public Optional<Like> findByIssueAndMember(Issue issue, Member member) {
        return data.stream()
                .filter(like -> like.getIssue().equals(issue))
                .filter(like -> like.getMemberId().equals(member.getId()))
                .findFirst();
    }

    @Override
    public boolean existsByIssueAndMember(Issue issue, Member member) {
        return data.stream()
                .anyMatch(like -> like.getIssue().equals(issue) && like.getMemberId().equals(member.getId()));
    }

    @Override
    public Like save(Like like) {
        if (like.getId() == null || like.getId() == 0) {
            Like createLike = Like.builder()
                    .id(autoGeneratedId.incrementAndGet())
                    .issue(like.getIssue())
                    .build();
            data.add(createLike);
            return createLike;
        } else {
            data.removeIf(item -> Objects.equals(item.getId(), like.getId()));
            data.add(like);
            return like;
        }
    }

    @Override
    public void delete(Like like) {
        data.removeIf(existingLike -> existingLike.getId().equals(like.getId()));
    }
}
